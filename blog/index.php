				<h2><a href="blog/?p=84" rel="bookmark" title="Permanent Link to Gilmore In Human Events: Property Rights Deserve Oval Office Attention">Gilmore In Human Events: Property Rights Deserve Oval Office Attention</a></h2>
				<small>June 25th, 2007 <!-- by Gilmore for President --></small>

				<div class="entry">
					<p>Gov. Jim Gilmore has written an op-ed on the topic of property rights in <a href="article.php?id=21270">Human Events</a>, make sure to read the article and learn more about Gov. Gilmore&#8217;s National Property Rights Initiative at <a href="../mygilmoresite/Property%20Rights">MyGilmore</a>:</p>
<blockquote>
<p align="left">Two years ago this week, the United States Supreme Court eroded our Fifth Amendment protections in the Kelo v. City of New London case by decreeing that government could seize your home to hand it over to private developers.</p>
<p>This was judicial activism at its worst. The Supreme Court essentially reworded the Constitution, making property rights conditional: a citizen had a right to his property only so long as he put it to use in a way the government deemed of economic value.<br />
As a lifelong conservative, I was offended by such an egregious judicial overreach. As a candidate for President, I believe it is past time for the federal government to step forward in defense of these cherished liberties in a manner our Founding Fathers would be proud of.</p>
<p>The Founding Fathers recognized that property rights were the foundation of a free society. The Virginia Declaration of Rights included property rights among the “inherent rights” of man. James Madison went so far as to define property as including not only a citizen’s possessions, but his opinions, religion, liberty and safety. It is no surprise, then, that the Founding Fathers enshrined the right to private property in the Fifth Amendment to the Constitution. As the Fifth Amendment guarantees, private property should be taken only for a &#8220;public use,&#8221; such as a school or road, not to build a private office building or garage The owner of that property, even when taken for public use, is entitled to just compensation.</p></blockquote>
<p><em><a href="article.php?id=21270">Click here to read the entire op-ed at HumanEvents.com</a></em>.</p>
