				<h2><a href="blog/?p=81" rel="bookmark" title="Permanent Link to Roxane discusses her trip to China">Roxane discusses her trip to China</a></h2>
				<small>June 20th, 2007 <!-- by Gilmore for President --></small>

				<div class="entry">
					<p class="MsoNormal">As a member of the President’s Committee on the Arts and Humanities, I was proud to be a part of the first <st1:country-region w:st="on">U.  S.</st1:country-region> delegation of federal cultural officials to <st1:country-region w:st="on"><st1:place w:st="on">China</st1:place></st1:country-region> from June 5 through 12, 2007.<span>  </span>Thirty-five public and private cultural leaders from the <st1:country-region w:st="on"><st1:place w:st="on">United States</st1:place></st1:country-region> met with Chinese Ministry of Culture officials to address expanding cultural relations between the two countries.<o:p> </o:p></p>
<p class="MsoNormal">The President’s Committee and the Ministry of Culture discussed cultural activities in each of our respective countries, and these discussions were underscored by events presented at on-site locations representing the diverse facets of Chinese culture, both ancient and traditional and modern.<span>  </span>Our visits included the <st1:placename w:st="on">Shanghai</st1:placename> <st1:placetype w:st="on">Museum</st1:placetype> and the Shanghai Children’s Palace; the Terra Cotta Warriors Museum and the newly opened Han Yang Ling Tombs in Xian; the Commune at the Great Wall architectural development, <st1:placetype w:st="on">Palace</st1:placetype> <st1:placetype w:st="on">Museum</st1:placetype>, the National Museum of Art, and a hard-hat tour of the new <st1:placename w:st="on">National</st1:placename> <st1:placetype w:st="on">Center</st1:placetype> for the Performing Arts in <st1:city w:st="on"><st1:place w:st="on">Beijing</st1:place></st1:city>.<o:p> </o:p></p>
<p class="MsoNormal">It was particularly interesting to see the similarity of both successes and difficulties the arts have both here and in <st1:place w:st="on"><st1:country-region w:st="on">China</st1:country-region></st1:place>. The arts foster individual fulfillment in retirement centers and youth after-school programs in <st1:country-region w:st="on">China</st1:country-region> just as they do in <st1:country-region w:st="on"><st1:place w:st="on">America</st1:place></st1:country-region>. But they struggle with the same issues of funding and historic preservation as we do.<span>  </span>Our committee&#8211;made up not only of public officials but also a large group of private citizens like me who are active because of a personal interest in the arts&#8211;hoped our participation would underscore the importance of private action in furtherance of cultural activities.<o:p> </o:p></p>
<p class="MsoNormal">I have always had a special interest in historic preservation, and I found the presentation by Vice Minister of State Administration of Cultural Heritage Shan Jixiang particularly interesting.<span>  </span>With the exploding Chinese population and industry, his department struggles daily with preserving historic <st1:country-region w:st="on"><st1:place w:st="on">China</st1:place></st1:country-region> from being erased by development and also by natural disasters such as floods.<span>  </span>Hosted by his department at the Commune by the Great Wall architectural development, I was pleased to have the opportunity to present Vice Minister Shan with a set of Jamestown 2007 Commemorative Stamps and briefly discuss the preservation of the fort at Historic Jamestown in Virginia.<o:p> </o:p></p>
<p class="MsoNormal">The China visit was an important opportunity for members of the President’s Committee, in particular the heads and representatives from the Library of Congress, the National Endowment for the Arts, the National Endowment for the Humanities, Institute of Museum and Library Services, as well as the Smithsonian Institution, to meet with Minister of Culture Sun Jiazheng, as well as other key Chinese officials, cultural administrators, artists, scholars and curators.<o:p> </o:p></p>
<p class="MsoNormal">Organized at the invitation of the Ministry of Culture of the People’s Republic of <st1:country-region w:st="on">China</st1:country-region> with the aim of expanding mutual understanding of the roles that the arts and humanities play in our respective societies, this was the President’s Committee’s first meeting in <st1:place w:st="on">Asia</st1:place>.<span>  </span>The PCAH’s cultural delegation visit was the culmination of previous U.S.-China cultural initiatives that began in 2000 with the U.S. State Department funded U.S./China Performing Arts Presenters Exchange Program.<span>  </span>With a new Implementing Accord for Cultural Exchange in place, these artistic and cultural relationships will evolve into more numerous and larger joint initiatives.<o:p> </o:p></p>
<p class="MsoNormal">The President’s Committee on the Arts and the Humanities was established by Executive Order in 1982.<span>  </span>The PCAH works with each administration to incorporate the arts and the humanities into White House objectives.<span>  </span>It bridges the bi-partisan interests of federal agencies and the private sector, supports special projects that increase participation, celebrates cultural excellence and helps incorporate the humanities and the arts into White House objectives.<span>  </span>It recognizes cultural excellence; engages in research and recognition programs; initiates special projects; and stimulates private funding.<span>  </span>Its primary federal cultural partners are the National Endowment for the Arts, the National Endowment for the Humanities, and the <st1:place w:st="on"><st1:placetype w:st="on">Institute</st1:placetype> of <st1:placename w:st="on">Museum</st1:placename></st1:place> and Library Services.<span>  </span>Expanding international cultural relationships is a primary focus, connecting the Americans with each other and the rest of the world.<span>  </span>First Lady Laura Bush is the Honorary Chairman of the PCAH and Adair Wakefield Margo of El Paso, TX, is the Chairman of the Committee.</p>
<p class="MsoNormal">Roxane Gatling Gilmore</p>
