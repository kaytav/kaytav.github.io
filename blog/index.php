				<h2><a href="blog/?p=85" rel="bookmark" title="Permanent Link to Gilmore Praises Senator Lugar&#8217;s Courage On Iraq">Gilmore Praises Senator Lugar&#8217;s Courage On Iraq</a></h2>
				<small>June 26th, 2007 <!-- by Gilmore for President --></small>

				<div class="entry">
					<p>WASHINGTON – Former Virginia Gov. Jim Gilmore today applauded U.S. Sen. Richard Lugar of Indiana for calling for a change in U.S. policy in Iraq.  Gilmore took a similar action a week ago.  Both Lugar and Gilmore said President Bush’s Iraq strategy is not working and both of them called for a downsizing of American military force in Iraq.</p>
<p>“I applaud Senator Lugar for his courageous comments today on the Iraq situation and I know that his comments came from the heart, as did mine,” Gilmore said.  “We can do better.  We can refocus our efforts toward protecting our national interests and away from policing Iraq neighborhoods with our valiant young military men and women.”</p>
<p>Gilmore said he hoped that in “the coming weeks many other key Republicans will join Senator Lugar and I in working for a limited drawdown of American forces in Iraq and redeployment geared to protecting the United States’ national interest and national security.”</p>
<p>Lugar said in his statement, “I suspect that for some Americans, benchmarks are a means of justifying a withdrawal by demonstrating that Iraq is irredeemable.” Gilmore in his letter to President Bush on June 16 made a similar comment, “Worst of all, we are starting to suggest that we will define “benchmarks”, which gives us an excuse for withdrawal if they cannot perform the impossible.”</p>
<p>Gilmore and Lugar also appear to be on the same page in their efforts to refocus America’s efforts in Iraq.  And Gilmore said today he hoped that other key Republicans would join with them in that effort.</p>
<p>Lugar’s proposal today said, “First, we have an interest in preventing Iraq or any piece of its territory from being used as a safe haven or training ground for terrorists or as a repository or assembly point for weapons of mass destruction.  Second, we have an interest in preventing the disorder and sectarian violence in Iraq from upsetting wider regional stability. Third, we have an interest in preventing Iranian domination of the region. The fall of Saddam Hussein’s Sunni government opened up opportunities for Iran to seek much greater influence in Iraq and in the broader Middle East. Fourth, we have an interest in limiting the loss of U.S. credibility in the region and throughout the world as a result of our Iraq mission.”</p>
<p>Like Gilmore, Lugar called for “a downsizing and re-deployment of U.S. military forces to more sustainable positions in Iraq or the Middle East. “</p>
<p>Gilmore’s letter to Bush last week said, “The U.S. has a stake in preventing a government from emerging that is expressly hostile to the U.S., such as in an al-Qaeda inspired coup.  The U.S. has a stake in not permitting the invasion and occupation of Iraq by any of its neighbors. I believe the only realistic alternative &#8212; the least bad option, if you will &#8212; is a limited deliberate drawdown of our military men and women and a redeployment of the forces remaining in the region to areas where they can more efficiently and effectively carry out a clearly defined mission.”</p>
				</div>

