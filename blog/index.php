				<h2><a href="blog/?p=80" rel="bookmark" title="Permanent Link to Bloggers Hail Gilmore Property Rights Initiative">Bloggers Hail Gilmore Property Rights Initiative</a></h2>
				<small>June 19th, 2007 <!-- by Gilmore for President --></small>

				<div class="entry">
					<p>WASHINGTON – Bloggers across the nation have hailed former Virginia Gov. Jim Gilmore’s announcement of a National Property Rights Initiative.  Gilmore unveiled the proposal last weekend at the National Taxpayers Union conference in Washington, DC.</p>
<p>“A candidate who gets it!  People keep asking me why I’m so high on Governor Gilmore and this is just another reason,” said blogger Wendell Robinson of Ohio.  “Kudos to the Governor for an initiative that gets to the heart and soul of what America is all about.”</p>
<p>In Iowa, Don McDowell of Cyclone Conservatives posted, “This happens to be an issue that I personally care a whole lot about and am very pleased to see that Governor Jim Gilmore is using whatever size bullhorn he has to try to make it a part of the public policy debate.”  Volokh Conspiracy, a blog on legal issues, said, “In general, his program looks pretty interesting and sensible…”</p>
<p>Gilmore’s National Property Rights Initiative includes a commitment to appoint judges who will strictly interpret the Constitution, as well as reissuing President Reagan’s Executive Order 12630, which required federal agencies to “first do no harm” to property rights when writing regulations.  Gilmore would also charge a Special Assistant in the White House with coordinating federal protections of property rights.</p>
