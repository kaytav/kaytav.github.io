				<small>July 2nd, 2007 <!-- by Gilmore for President --></small>

				<div class="entry">
					<p>SPRINGFIELD, VA – The presidential campaign of former Virginia Gov. Jim Gilmore announced today that Gilmore received emergency eye surgery Friday to repair a partially detached retina in his right eye.</p>
