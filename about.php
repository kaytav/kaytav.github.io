										<div class="subtitle">Courageous. Consistent. Conservative.</div>
										<div class="subtext">
											<p>
												<img src="images/gilmoreandwife_pic.jpg" alt="Jim Gilmore &quot; Wife" style="float:left" />
												<p class="firstLetter">Former Virginia Gov. Jim Gilmore is the answer to the question being asked by Republicans all across the country: &ldquo;Is there a strong, steady, electable, conservative Republican candidate for President of the United States?&rdquo;</p>
												<p>Jim Gilmore, 57, has exactly the credentials that most Republicans are looking for in their nominee for President. Jim Gilmore is the leader Republicans can trust to stand for low taxes, secure borders, a strong national defense and the preservation of traditional family values.</p>
												<p>Jim Gilmore is a &ldquo;can do&rdquo; leader who has devoted his life to the traditional values and principles that have been the hallmark of successful Republican candidates while focusing on technology and innovative ideas as the pathway to solutions for many of the problems facing America today.</p>
												<p>A native of Richmond Virginia, Jim Gilmore is the son of a working class parents who served as a member of U.S. Army counter-intelligence; a tough crime busting prosecutor in Virginia; a popular no-nonsense Virginia Attorney General; and a former Governor who became a champion of the taxpayers by reducing taxes on working men and woman by $1.5 billion.</p>
												<p>A graduate of the University of Virginia and the University of Virginia School of Law, Jim Gilmore is a former chairman of the Republican National Committee. Appointed by Congress as the chairman of a Congressionally created national commission charged with making recommendations on ways to prevent and respond to terrorism, which became known as the Gilmore Commission, Jim Gilmore has become a nationally known authority on homeland security.</p>
												<p>Born in the historic fan district of Richmond on Oct. 6, 1949, Jim Gilmore is the son of a Safeway meatcutter and a church secretary. Jim Gilmore graduated from J.R. Tucker High School and studied foreign policy at the University of Virginia. While at UVA, Jim Gilmore joined College Republicans, and was elected a statewide officer, and met his wife Roxane. He and Roxane today are the parents of two sons, Jay and Ashton.</p>

												<br />

												<p class="firstLetter">After graduating from the University of Virginia, Jim Gilmore volunteered for the Army. He graduated with honors from the Army Intelligence School and the Defense Language Institute and was assigned to the 650th Military Intelligence Group. Fluent in German, he served in the U.S. Army&rsquo;s counter-intelligence unit in Germany during the Vietnam-era. Jim Gilmore also is a former Chairman of the Board of Trustees of the U.S. Air Force Academy, having been appointed by President Bush and elected chairman by his fellow board members.</p>
												<p>Returning to Virginia, Jim Gilmore entered the University of Virginia School of Law and graduated in 1977, beginning practice with a small Richmond law firm. After opening his own firm and private practice, Jim Gilmore ran for prosecutor in Henrico County, Virginia and was elected in 1987 and reelected in 1991, establishing an impressive record as a crime fighter.</p>
												<p>In 1993, Jim Gilmore ran for the office of Virginia Attorney General. Although he was considered a long shot for both the nomination and the general election, he won both contests and was elected Attorney General by a substantial margin. As Attorney General, Jim Gilmore was a legal champion of the taxpayers launching successful investigations into Medicaid fraud, government waste and consumer protection. He also led a nationwide effort to stop arson attacks against African-American churches.</p>
												<p>In 1997, Jim Gilmore was again an underdog when he announced his candidacy for Governor of Virginia. But campaigning on traditional Republican principles of limited government, tax cuts and family values, he was overwhelmingly elected, winning 56 percent of the vote. As Governor, Jim Gilmore enacted and signed into legislation five different tax cuts including a 70 percent cut of the infamous car tax by $1.1 billion &ndash; the largest tax cut in Virginia history.</p>
												<p>On key issues, Governor Jim Gilmore stood his ground time and time again to preserve for Virginians the important taxpayer rights and traditional values that are essential to making sure that government is the servant, not the master of its citizens. Jim Gilmore created Virginia Exile to get guns out of the hands of criminals. And he created the first cabinet level Department of Technology to help Virginia move swiftly into the future.</p>
												<p>Governor Jim Gilmore directed the state&rsquo;s response when Virginia was attacked on 9/11. As a follow up to those efforts and to his nationally recognized work on homeland security as chairman of the Gilmore Commission, Gilmore currently serves as chairman of the National Council on Readiness and Preparedness (NCORP).</p>

												<br />
	
												<p class="firstLetter">NCORP&rsquo;s mission, under Jim Gilmore&rsquo;s leadership, is to establish public/private partnerships that help strengthen America&rsquo;s homeland security. Jim Gilmore also serves as president of USA Secure, a non-profit homeland security think tank that works to strengthen and coordinate communication between the government, especially the Department of Homeland Security, private industry, first responders, national security think tanks and universities.</p>
                  								<p>In August of 2006, a grassroots Draft Gilmore effort was formed by volunteers to make the case that Jim Gilmore is the most consistent conservative among major Republican candidates for President. On December 19, 2006, Jim Gilmore announced the formation of a presidential exploratory committee.</p>
											</p>
